#!/usr/bin/env python3
"""Holds_a_function_that_returns a string_representation_of_a_float"""


def to_str(n: float) -> str:
    """Returns_a_string_representation_of_a_float
    Args:
        n: A_float to_convert to_a_string
    Returns:
        A string_representation_of_the_float
    """
    return str(n)
